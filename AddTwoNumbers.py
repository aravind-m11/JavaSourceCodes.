def main():
    # Input two integers from the user
    number1 = int(input("Enter the first number: "))
    number2 = int(input("Enter the second number: "))

    # Add the two numbers
    sum = number1 + number2

    # Display the sum
    print(f"The sum of {number1} and {number2} is: {sum}")

if __name__ == "__main__":
    main()